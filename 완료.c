int boss_attack(character* player1, boss* boss1)
{
    int damage = boss1->attack;
    player1->now_HP -= damage;
    if (player1->now_HP < 0)
    {
        player1->now_HP = 0;
    }
    printf("%s가 %d의 피해를 입혔습니다!\n",boss1->monster_name,damage);
    return player1->now_HP == 0;
    system("clear");
}


int player_attack(character* player1, boss* boss1)
{
    int damage = player1->attack_power;
    boss1->now_HP -= damage;
    if(boss1->now_HP < 0)
    {
        boss1->now_HP = 0;
    }
    printf("%s가 %d의 피해를 입혔습니다!\n",player1->nickname, damage);
    return boss1->now_HP == 0;
    system("clear");
}


